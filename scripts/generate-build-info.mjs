#!/usr/bin/env node
/**
 * Generates apps/octra/src/app/build-info.ts from:
 *   - version  → root package.json
 *   - hash     → short git rev (or 'dev' outside a git checkout)
 *   - timestamp → current ISO time
 *
 * Wired into `prebuild`, `prestart:octra`, and `postinstall` so the
 * navbar's "VISP OCTRA" version popover stays aligned with package.json
 * whenever the app is started, built, or freshly installed.
 */
import { execSync } from 'node:child_process';
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, '..');
const pkgPath = resolve(repoRoot, 'package.json');
const outPath = resolve(repoRoot, 'apps/octra/src/app/build-info.ts');

const pkg = JSON.parse(readFileSync(pkgPath, 'utf8'));
const version = pkg.version ?? '0.0.0';

let hash = 'dev';
try {
  hash = execSync('git rev-parse --short HEAD', {
    cwd: repoRoot,
    stdio: ['ignore', 'pipe', 'ignore'],
  })
    .toString()
    .trim();
} catch {
  // not a git checkout — leave 'dev'
}

const timestamp = new Date().toISOString();

const banner = `// Generated by scripts/generate-build-info.mjs.
// Do not edit — regenerated on prebuild / prestart / postinstall.
//
// version is captured from the root package.json at script-run time.
// We cannot import package.json directly from this file because doing
// so walks above apps/octra's tsconfig rootDir and breaks Angular's
// Ivy build (every project file becomes "missing from compilation").
// The hooks ensure the file is refreshed whenever the app starts,
// builds, or after \`npm install\`.
`;

const body = `export interface BuildInfo {
  version: string;
  hash: string;
  timestamp: string;
}

export const BUILD_INFO: BuildInfo = {
  version: ${JSON.stringify(version)},
  hash: ${JSON.stringify(hash)},
  timestamp: ${JSON.stringify(timestamp)},
};
`;

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, banner + '\n' + body, 'utf8');

console.log(`build-info.ts: version=${version} hash=${hash}`);
